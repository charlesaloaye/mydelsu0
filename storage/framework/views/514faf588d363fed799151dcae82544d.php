<script>
    // Optional: Add active state functionality
    document.addEventListener('DOMContentLoaded', function() {
        const navItems = document.querySelectorAll('.nav-item');
        const currentPath = window.location.pathname;

        navItems.forEach(item => {
            const link = item.querySelector('a');
            const href = link.getAttribute('href');

            // Remove active class from all items
            item.classList.remove('active');

            // Add active class to current page (skip WhatsApp link)
            if (!href.includes('wa.me') && (currentPath === href || (currentPath === '/' && href ===
                    '/news'))) {
                item.classList.add('active');
            }
        });

        // Add click handlers for smooth interactions (except WhatsApp)
        navItems.forEach(item => {
            const link = item.querySelector('a');
            const href = link.getAttribute('href');

            if (!href.includes('wa.me')) {
                item.addEventListener('click', function() {
                    navItems.forEach(nav => {
                        const navLink = nav.querySelector('a');
                        if (!navLink.getAttribute('href').includes('wa.me')) {
                            nav.classList.remove('active');
                        }
                    });
                    this.classList.add('active');
                });
            }
        });
    });
</script>
